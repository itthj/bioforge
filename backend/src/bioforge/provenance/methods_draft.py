@dataclass
class MethodsDraft:
    """Everything the scientist needs to paste into a manuscript + Supplementary Methods."""

    paragraph: str          # Validated methods paragraph — past tense, inline citations [1][2]…
    bibtex_block: str       # All cited entries concatenated — paste into .bib file
    param_table_md: str     # Markdown table for Supplementary Methods
    warnings: list[str] = field(default_factory=list)
    trace_id: str = ""


# ---------------------------------------------------------------------------
# Parameter extraction helpers
# ---------------------------------------------------------------------------

def _params_by_tool_order(result: AgentResult | None) -> list[dict[str, Any]]:
    """Return tool_input dicts aligned with manifest.tools (same tool-call order)."""
    if result is None:
        return []
    return [
        s.tool_input or {}
        for s in result.steps
        if s.type == "tool_call" and s.tool_name is not None
    ]


def _pv(value: Any, max_chars: int = 50) -> str:
    """Compact parameter value for the supplementary table."""
    s = str(value)
    if len(s) > max_chars:
        return f"{s[:max_chars]}… ({len(s)}\u00a0chars)"
    return s


# ---------------------------------------------------------------------------
# Citation registry — builds ordered cite list and [N] index
# ---------------------------------------------------------------------------

class _CitationList:
    def __init__(self) -> None:
        self._seen: dict[str, int] = {}  # key → 1-based index
        self._keys: list[str] = []

    def add(self, key: str) -> str:
        """Register a citation key and return its [N] mark."""
        if key not in self._seen:
            self._seen[key] = len(self._keys) + 1
            self._keys.append(key)
        return f"[{self._seen[key]}]"

    @property
    def bibtex_block(self) -> str:
        entries = [_BIBTEX[k] for k in self._keys if k in _BIBTEX]
        return "\n\n".join(entries)


# ---------------------------------------------------------------------------
# Grounding guard
# ---------------------------------------------------------------------------

# Small numbers (single digits) appear freely in prose without being benchmark facts.
_IGNORE_NUMS = {str(i) for i in range(10)}


def _grounding_guard(
    template_prose: str,
    llm_prose: str,
    mandatory_caveats: list[str],
) -> tuple[bool, list[str]]:
    """Return (valid, issues).  valid=True → LLM output is safe to use."""
    # Extract every multi-digit number and version-like string from template (ground truth)
    fact_nums = set(re.findall(r"\b\d{2,}\.?\d*\b", template_prose))
    fact_vers = set(re.findall(r"\bv\d[\d.]+\b", template_prose, re.I))
    fact_nums |= fact_vers

    issues: list[str] = []

    # 1. Numbers in LLM output must all exist in template facts
    for num in re.findall(r"\b\d{2,}\.?\d*\b|\bv\d[\d.]+\b", llm_prose, re.I):
        if num not in _IGNORE_NUMS and num not in fact_nums:
            issues.append(f"Ungrounded token in LLM output: {num!r}")

    # 2. Every mandatory caveat's key phrase must survive
    for caveat in mandatory_caveats:
        if caveat.lower() not in llm_prose.lower():
            issues.append(f"Missing mandatory caveat: {caveat[:60]!r}…")

    return (not issues), issues


# ---------------------------------------------------------------------------
# LLM prose polish
# ---------------------------------------------------------------------------

_POLISH_SYSTEM = (
    "You are a scientific editor improving the fluency of a computational methods section "
    "for a peer-reviewed manuscript. You ONLY improve sentence flow and tense consistency. "
    "You NEVER add, remove, or change any number, date, version string, database name, "
    "citation mark, or accuracy caveat. Return only the edited paragraph — no commentary."
)

_POLISH_USER = """\
Rewrite the following methods text as natural scientific prose in past tense. \
Improve fluency and sentence variety only. \
All numbers, version strings, citation marks, and accuracy caveats must be preserved exactly.

{prose}"""


async def _llm_polish(prose: str) -> str:
    """Call the Anthropic API for a language-only fluency pass.  Returns original on any error."""
    try:
        import anthropic
        from bioforge.config import settings

        client = anthropic.AsyncAnthropic(api_key=settings.anthropic_api_key)
        msg = await client.messages.create(
            model="claude-haiku-4-5",  # cheapest model — language only, no reasoning needed
            max_tokens=1024,
            system=_POLISH_SYSTEM,
            messages=[{"role": "user", "content": _POLISH_USER.format(prose=prose)}],
        )
        return msg.content[0].text.strip()
    except Exception:
        return prose  # silent fallback — template prose is always valid


