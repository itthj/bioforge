"""Enhanced methods draft for manuscript submission.

Extends the generic ``render_methods_report`` with four things that the generic version
deliberately omits (it stays minimal so it's always correct):

  1. Per-tool prose templates  — "on-target efficiency was scored using DeepCRISPR [2]"
                                  rather than "tool `score_guide_on_target` was run".
  2. Benchmark accuracy numbers — hardcoded constants pulled from the committed benchmark
                                  artifacts, never generated by an LLM.
  3. Mandatory accuracy caveats — injected verbatim and validated to survive the polish step.
  4. BibTeX citation block      — copy-paste into any reference manager.
  5. Supplementary parameter table — every parameter, ready for a Supplementary Methods.

Pipeline
--------
  RunManifest + AgentResult
        │  deterministic
  _fill_templates()   ← per-tool sentences, hardcoded benchmark constants
        │  optional
  _llm_polish()       ← language only; strict prompt forbids new/changed facts
        │  guard
  _grounding_guard()  ← reject if output introduces ungrounded numbers or drops caveats
        │
  MethodsDraft        ← paragraph | bibtex_block | param_table_md | warnings

Design rules (mirroring provenance.methods_report):
  - Pure function, no I/O, no network.
  - Every number in the output traces to either the run manifest (runtime data) or
    a hardcoded _BENCHMARK constant (committed, review-gated).  The LLM touches only prose style.
  - Where a tool is unknown, it is summarised generically (no invented accuracy claims).
"""

